function updateAcceleration(a) {
    var shakeParamX = parseInt(Math.abs(roundNumber(a.x)),10);
    var shakeParamY = parseInt(Math.abs(roundNumber(a.y)),10);
    var shakeParamZ = parseInt(Math.abs(roundNumber(a.z)),10);

    if (shakeParamX > 5 && shakeParamY > 5 && shakeParamZ > 5) {
        onShake();
    }

}

var toggleAccel = function() {
    if (accelerationWatch !== null) {
        navigator.accelerometer.clearWatch(accelerationWatch);
        updateAcceleration({
            x : "",
            y : "",
            z : ""
        });
        accelerationWatch = null;
    } else {
        var options = {};
        options.frequency = 500;
        accelerationWatch = navigator.accelerometer.watchAcceleration(
                updateAcceleration, function(ex) {
                    alert("accel fail (" + ex.name + ": " + ex.message + ")");
                }, options);
    }
};

